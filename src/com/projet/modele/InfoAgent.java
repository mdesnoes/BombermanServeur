package com.projet.modele;


public class InfoAgent {
	
	private int x;
	private int y;
	private char type;

	
	public InfoAgent() {
	}
	
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public char getType() {
		return type;
	}
	public void setType(char type) {
		this.type = type;
	}

}
	